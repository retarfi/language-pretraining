# override transformers.integrations.TensorBoardCallback to avoid warnings to keys: elapsed_time and 
def on_log(self, args, state, control, logs=None, **kwargs):
    if not state.is_world_process_zero:
        return

    if self.tb_writer is None:
        self._init_summary_writer(args)

    if self.tb_writer is not None:
        logs = rewrite_logs(logs)
        for k, v in logs.items():
            if isinstance(v, (int, float)):
                self.tb_writer.add_scalar(k, v, state.global_step)
            elif k in [x + y for x in ['', 'train/'] for y in ['elapsed_time', 'current_time', 'remaining_time']]:
                continue
            else:
                logger.warning(
                    "Trainer is attempting to log a value of "
                    f'"{v}" of type {type(v)} for key "{k}" as a scalar. '
                    "This invocation of Tensorboard's writer.add_scalar() "
                    "is incorrect so we dropped this attribute."
                )
        self.tb_writer.flush()

TensorBoardCallback.on_log = on_log
